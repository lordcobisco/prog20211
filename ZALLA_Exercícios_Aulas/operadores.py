# Atribuição e operadores aritméticos

resultadoSoma = 12+55 #Atribuição
print(resultadoSoma)

print(12+55) #Soma

print(12-55) #Subtração

print(10*55) #Multiplicação

print(55/5) #Divisão

print(12**2) #Exponenciação

print(55//7) #Parte inteira da divisão

print(55%7) #Resto da divisão

